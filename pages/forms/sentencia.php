<h3>I. El PROCESO</h3>

<li>1. 	Fue presentado el libelo de la demanda por el esposo el 17 de Febrero de 2016, causa que fue admitida  a  tramitación  por  decreto  del  señor  Vicario  judicial. El fundamento de la misma se hallaba en matrimonio contraído por miedo grave, para librarse  de cual alguien  se vea obligado a casarse, en ambas partes.
Ratificada que fue la demanda por la actora, la convenida, fue citada mediante edicto, el 25 de Abril de 2016, dado que su domicilio se ignora, se publicó en el periódico “Jornada” siendo la función de este medio escrito de publicar edictos, durante tres días hábiles, para la comparecencia ante este tribunal. Como no se presentó fue declarada rebelde. </li>

<li>2.  La fórmula de la duda fue determinada el 3 de Mayo de 2016, aceptando la propuesta por el actor en los siguientes términos: “SI CONSTA LA NULIDAD DEL MATRIMONIO, EN EL CASO POR: 1.- el matrimonio contraído por violencia o por miedo grave, para liberarse del cual alguien se vea obligado a casarse (c1101), en ambas partes ”.
La instrucción de la causa comenzó el 19 de Febrero del 2016, actuando como juez instructor el Pbro. Dr Roberto Boceta Fernández y el 10 de Mayo de 2016 se decretó la conclusión y publicación de la causa. Se  renunció  al  alegato  de  bien probado  por  la parte actora y  finalmente  fue  presentado  el  último informe del Defensor del vínculo que fue favorable a la nulidad.
