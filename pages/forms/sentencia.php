<p align="justify"> En la ciudad de La Paz y Sala del Tribunal Eclesiástico Regional de La Paz, en el cuarto año de su Pontificado de Su Santidad el Papa Francisco I, siendo Arzobispo de la arquidiócesis el Excmo. y Rvdmo. Sr. Dr. Edmundo Abastoflor Montero, el  infrascrito Juez único, Pbro. Dr. Roberto Boceta Fernández Vicario judicial del Arzobispado y Presidente y Ponente, habiendo intervenido como Defensor del Vínculo el que lo es de este Arzobispado el Pbro. Lic. Florencio Flores, después de haber visto y atentamente examinado los autos del juicio de declaración de nulidad de matrimonio, seguida por Don Guillermo López Susaño, como actor, con domicilio en Red. San Julián S/C, representado y defendido por si mismo, contra su esposa Doña Pastora Oyardo Mamani, con domicilio desconocido, que ha sido citada mediante edictos y no compareciendo, declarada ausente. El matrimonio se celebró en la Parroquia de San Pedro de Sica Sica, Prelatura de Corocoro, el 14 de Julio de 1990, por lo que este tribunal es competente. El juez único ha dictado la siguiente sentencia definitiva en primer grado de jurisdicción.

