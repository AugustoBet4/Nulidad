<h3>III. FUNDAMENTOS JURÍDICOS</h3>

<h3>I.  COACCION MORAL O MIEDO GRAVE</h3>

<li>12.	El c. 1103 establece que: <i> Es inválido el matrimonio contraído por violencia o miedo grave proveniente de una causa externa, incluso el no inferido de propio intento, para librarse del cual alguien se vea obligado a elegir el matrimonio.</i>
	En cuanto al segundo  capítulo del Dubio c. 1103, primero veremos la definición de Coacción Moral según el legislador. Es la presión psicológica que, mediante la amenaza, el agente ejerce sobre el paciente. La coacción moral y el miedo se relacionan en términos de causa y efecto. En consecuencia, mientras la violencia opera sobre la expresión externa del consentimiento, la coacción moral y su efecto, el miedo, actúan sobre el interior psicológico del contrayente y vician la génesis del consentimiento. La coacción  moral supone un vicio en el consentimiento; porque el sujeto acepta el matrimonio si bien por causa del miedo originado, por la amenaza de unos males. El legislador establece la invalidez del consentimiento prestado por violencia, coacción moral o por miedo grave (Comentario al Código de Derecho, Ediciones Universidad de Navarra, Pamplona-España 1992).
</li>
